     <h2 class="pageID">Parallax Research</h2>
        
     <p>Parallax scrolling is a technique, where a web page’s background is moving at a slower rate than the foreground. This creates an illusion of depth to the page, giving the content a 3D effect. This future is remarkable and adds dynamic to the website when used skillfully it may have “wow” effect. Site stops to be boring source of info and becomes a relation between viewer and the site on a different level. Viewer is attracted by the site and starts to follow it. </p>

    <p>Gail Hopkins says “More specifically, in terms of animation capabilities, there are 4 ways in which the parallax can be implemented:</p>
    <ul>
        <li>Raster Parallax, which is mostly used to give an optical illusion of movement</li>
        <li>Repeat Pattern, that uses scrolling displays over a repeated background</li>
        <li>Layered Parallax, where the foreground and background objects move at a different pace</li>
        <li>Or, the simple Sprite Method (Source: Envato blog)”</li>
    </ul>

    <p>It is important to remember that adding such an interesting feature to our website affects Search Engine Optimization and load time. This is the price to be paid if needed. </p>

    <p>According to Sufan bin Uzayar “Parallax scrolling works well only if your website requires one-time visits. If you’re expecting repeat visitors, or looking to convert visitors into customers, parallax scrolling will not look impressive, nor will it help you gain more visitors.(...)”
    And another time he says “In general, on-site SEO schema follows a specific pattern. You can follow a definite SEO architecture with parallax websites, but the biggest and most visible drawback in this case is the fact that parallax scrolling is not the best fit for mobile devices.”(Uzayar)</p>

    <p>So before we make a decision whether we want parallax on our website or not let’s first look what type of website we want and what type of product we are dealing with.</p>




