     <h2 class="pageID">Galleries Research</h2>
     <p>When choosing what kind of gallery you will have on your website you need to take into consideration what is most important for your clients. You must balance between quality of pictures and its download time, does your client needs to know every detail? will zoom be needed or more important is overall picture design, what type of machine, operating system,  generation, internet connection your prospect clients are using. </p>
<p>According to Tori M. “Graphic designers (especially ours) always want to ensure top quality images, but higher quality means more disk space means higher load time, and you know what the experts say: Users tend to abandon websites that don’t load within 3 seconds. The ideal load time for websites is under 2 seconds. 1 second is best, but not many sites load that quickly.”(Metzger)</p>
<p>It is recommended to track website’s loading time by checking performance data in Google Analytics or Network tab(right click) or using special speed test tools like Pingdom. If you need to shorten loading time start with lowering picture quality before saving it, in photoshop you could change the amount of pixels or you could use Better WordPress Minify. According to Khang Minh “This plugin uses the PHP library Minify and relies on WordPress’s enqueuing system rather than the output buffer, which respects the order of CSS and JS files as well as their dependencies. BWP Minify is very customizable and easy to use”(Minh).</p>
        
<p>Also it is important what type of image file types you are loading to your gallery. Stay with JPEG if you can, as those tend to be the smallest in size, they are best suited for photographic images. PNG images have some transparency and PNG-24 when simple images and photographic elements are combined. GIF are small animations or with bigger color palette.</p>
        
<p>Drew C. says “Sometimes the best way to decrease image load time is not to use an image at all. Improvements to CSS have made it possible for the browser to render certain “images” using pure CSS. It is now possible to generate rounded rectangles, gradients, drop shadows, and transparent images using CSS”(Coffin).</p>

        
